// Defining a function
function addNumbers(a, b) {
    return a + b;
}
// Using anonymous function
var addNumbers = function(a, b) {
    return a + b;
}
// using Arrow Functions with return statement
var addNumbers = (a, b) => {
    return a + b;
}
// Using Arrow Functions without return statements and without curly braces
var addNumbers4 = (a, b) => a + b; // implicit return statement

//arrow functions is also known as fat arrows
//arrow function are anonymous --> meaning the function doesn't have a name like a regular function